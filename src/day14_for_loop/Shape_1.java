package day14_for_loop;

public class Shape_1 {
    public static void main(String[] args) {
        for (int i = 0; i <=7 ; i++) {

            for (int j = 0; j <=5 ; j++) {
                System.out.print("* ");
            }
            System.out.println();

        }
    }

}
